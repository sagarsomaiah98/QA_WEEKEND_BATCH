package Loops;

public class switch_Ex {

	public static void main(String[] args) {
	
		String grade="D";
		
		switch(grade) 
		{
		case "A":
			System.out.println("first");
			break;
		case "B":
			System.out.println("second");
			break;
		case "C":
			System.out.println("third");
			break;
		case "D":
			System.out.println("fail");
			break;
		default:
			System.out.println("NA");
		
		}
		
		

	}

}
