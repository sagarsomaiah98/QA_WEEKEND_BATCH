package Operators;

public class Math_Operator {



	public static void main(String[] args) {
		
		int i=20;
		
		int j=10;
		
		System.out.println(i+j);
		System.out.println(i-j);
		System.out.println(i*j);
		System.out.println(i/j);
		System.out.println(i%j);//modulus
		
		System.out.println(j/i);
	

	}

}
