package Operators;

public class Relational_Operator {

	public static void main(String[] args) {
		
		int u=100;
		int m=200;
		int j=100;
		
		System.out.println(u<m);
		System.out.println(u>j);
		System.out.println(u<m);
		System.out.println(u<j);
		System.out.println(u==m);
		System.out.println(u==j);
		System.out.println(u<=m);
		System.out.println(u<=j);
		System.out.println(u>=j);
		System.out.println(u>=m);
		

	}

}
